MAISON POUR TOUS asbl a pour objectif  de:
-	Réduire les risques de violence basée sur les genres en mettant en œuvre des stratégies des préventions et d’atténuations,
-	Favoriser la résilience en renforçant les systèmes communautaires et nationaux visant à prévenir et à atténuer les violences basées sur le genre tout en permettant aux survivants (victimes) et aux personnes les plus exposées à cette forme de violence d’accéder à un accompagnement et aux soins,
-	Faciliter le relèvement des communautés et des sociétés en soutenant les capacités locales et nationales enfin de trouver des solutions durables au problème de violence basée sur le genre,
-	Participer à l’instauration d’une société moins violente.
-	Entretenir  des relations de collaboration avec des associations et des organismes tant nationaux qu’internationaux qui militent pour le droit de la femme.
-	Promouvoir le respect de la vie, et des valeurs humaines positives qui prennent en compte la différence des deux sexes, 
-	Promouvoir les droits de la femme et de la jeune fille, 
-	Promouvoir l’autonomisation de la femme et le  statut social équitable,
-	Aider les personnes des troisièmes âges.
-	Aider les filles mères a s’auto prendre en charge.    
